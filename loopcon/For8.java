package loopcon;

public class For8 {
  public static void main(String[] args) {

    //별찍기

    for (int i=0; i<5; i++){
      for (int j=i; j<5; j++){
        System.out.print(" * ");
      }//제이끝
      System.out.println();
    }//아이끝


  }//
}






